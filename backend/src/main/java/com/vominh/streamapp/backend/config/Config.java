@Configuration
@EnableJpaRepositories(basePackages = {"com.vominh.streamapp.backend.repository"})
@EntityScan(basePackages = {"com.vominh.streamapp.backend.entity"})
public class Config {
    /*
        Disable CORS
     */
    @Bean
    public WebMvcConfigurer corsConfigurer() {
        return new WebMvcConfigurerAdapter() {
            @Override
            public void addCorsMappings(CorsRegistry registry) {
                registry.addMapping("/**")
                        .allowedMethods("HEAD", "GET", "PUT", "POST", "DELETE", "PATCH");
            }
        };
    }
}





